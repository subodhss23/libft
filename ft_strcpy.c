#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	size_t	i;
	char	*s;

	i = 0;
	s = (char*)src;
	while (s[i] != '\0')
	{
		dst[i] = s[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
